/* Datatypes and their sizes */
#include <iostream>

using namespace std;

int main (){

    // cout is print function in c++
    cout << "Size of an int is: " << sizeof(int) << endl; // int is 4 bytes 
    cout << "Size of an char is: " << sizeof(char) << endl; // 1 b
    cout << "Size of a float is: " << sizeof(float) << endl; // 4b
    cout << "Size of an short int is: " << sizeof(short int) << endl; // 2b
    cout << "Size of an long int is: " << sizeof(long int) << endl; //8b
    cout << "Size of an double is: " << sizeof(double) << endl; // 8b

return 0;

}